/**
 * アイコンサイズの共通定数（px 単位）。
 * lucide-react などの `size` prop へ渡す数値を一元管理する。
 *
 * ハードコードを避け、用途に応じたセマンティックなトークンで指定すること。
 */
export const ICON_SIZE = {
  /** 12px — バッジ、パンくず、行内補助アイコン */
  xs: 12,
  /** 14px — 一般的なボタン内・リスト項目のアイコン（デフォルト） */
  sm: 14,
  /** 16px — ナビゲーション、中サイズのコントロール */
  md: 16,
  /** 18px — 管理画面ヘッダーなど */
  lg: 18,
  /** 20px — モバイルメニュー、主要コントロール */
  xl: 20,
  /** 24px — ページヘッダー、ランディング上のカード */
  xxl: 24,
  /** 28px — ヒーローセクションのアイコン */
  hero: 28,
} as const;

export type IconSize = (typeof ICON_SIZE)[keyof typeof ICON_SIZE];
